package Mesh;

import javafx.scene.paint.Color;

public class LeafDescription {
    public Color color;
    public double r1;
    public double r2;
    public double tilt;

    public LeafDescription(Color color, double r1, double r2, double tilt){
        this.color = color;
        this.r1 = r1;
        this.r2 = r2;
        this.tilt = tilt;
    }
}


